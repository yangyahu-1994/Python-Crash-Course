# 创建变量
age = 26

# 条件判断
if age < 2:
    print("He is a baby.")
elif 2 <= age < 4:
    print("\nHe is learning to walk.")
elif 4 <= age < 13:
    print("\nHe is a child.")
elif 13 <= age < 20:
    print("\nHe is a teenager.")
elif 20 <= age < 65:
    print("\nHe is an adult.")
else:
    print("\nHe is old.")